    private void loadDataToTable() {
        userConnect UserConnect = new userConnect();
        List<User> loadUser = UserConnect.loadUser();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows in the table

        // Add each item to the table
        for (User user : loadUser) {
            model.addRow(new Object[]{user.getName(), user.getEmail(), user.getUserType(), user.getUsername(), user.getPassword()});
        }

    }

