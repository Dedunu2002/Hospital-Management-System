    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a user to delete.");
        return;
    }
    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this user account?", "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String Name = model.getValueAt(selectedRow, 0).toString(); // Get user name
        
        // Remove from database
        userConnect UserConnect = new userConnect();
        boolean success = UserConnect.deleteUser(Name);
        
        if (success) {
            model.removeRow(selectedRow); // Remove from table
            javax.swing.JOptionPane.showMessageDialog(this, "User account deleted successfully.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error deleting patient.");
        }
    }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

